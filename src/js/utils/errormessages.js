export function networkErrorMessage() {
  return "Network error: Please check your internet connection and try again.";
}

export function serverErrorMessage() {
  return "Server error: We're having trouble processing your request right now. Please try again later.";
}

export function badRequestErrorMessage() {
  return "Bad Request: The data sent to the server was invalid. Please check your input and try again.";
}

export function serviceUnavailableMessage() {
  return "Service Unavailable: The server is currently unable to handle the request. Please try again later.";
}
